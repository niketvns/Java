public class datatypes {
    public static void main(String[] args) {
        // Primitive Data types
        // 1. Integer Data types
        int n = 2;
        byte bt = 126;
        short sh = 4234;
        long l = 442342l; // In long we need to use 'l' at the end of number.

        // 2. Float Data types
        float f = 5.8f; // In float, we need to use 'f' at the end of number
        double d = 5.8;

        // 3. Character
        char c = 'K';

        // 4. Boolean
        boolean isTrue = true;

        System.out.println("Hello World");
    }
}
